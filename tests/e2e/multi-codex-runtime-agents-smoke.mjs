import { mkdir, mkdtemp, readFile, rm, writeFile } from 'node:fs/promises';
import { tmpdir } from 'node:os';
import { join } from 'node:path';
import {
  api,
  buildServer,
  createSessionAndWaitForBrief,
  listEvents,
  startSmokeServer,
  stopSmokeServer,
  waitForStatus
} from './smoke-server.mjs';

await buildServer();

let server;
let workspaceRoot;

try {
  workspaceRoot = await mkdtemp(join(tmpdir(), 'agent-cluster-multi-codex-'));
  await mkdir(join(workspaceRoot, 'src'), { recursive: true });
  await writeFile(
    join(workspaceRoot, 'package.json'),
    JSON.stringify({ scripts: { test: 'node test-smoke.mjs' } }, null, 2)
  );
  await writeFile(join(workspaceRoot, 'src', 'frontend.txt'), 'frontend before\n');
  await writeFile(join(workspaceRoot, 'src', 'backend.txt'), 'backend before\n');
  await writeFile(
    join(workspaceRoot, 'test-smoke.mjs'),
    [
      "import { existsSync } from 'node:fs';",
      "console.log('multi codex tests passed');",
      "if (!existsSync('src/frontend.txt')) throw new Error('frontend file missing');",
      "if (!existsSync('src/backend.txt')) throw new Error('backend file missing');"
    ].join('\n')
  );

  const stubScript = join(workspaceRoot, 'codex-multi-stub.mjs');
  const stubCommand = join(workspaceRoot, process.platform === 'win32' ? 'codex-multi-stub.cmd' : 'codex-multi-stub.sh');
  await writeFile(
    stubScript,
    [
      "import { mkdirSync, writeFileSync } from 'node:fs';",
      "const prompt = process.argv.slice(2).join(' ');",
      "const requiredKind = process.env.AGENT_CLUSTER_EXPECTED_OUTPUT_KIND ?? prompt.match(/Required output kind:\\s*([a-z_]+)/i)?.[1] ?? prompt.match(/expectedOutput[\\s\\S]{0,240}(task_acceptance_decision|task_claim_decision|task_execution_result)/i)?.[1] ?? 'task_execution_result';",
      "const isClaim = requiredKind === 'task_acceptance_decision' || requiredKind === 'task_claim_decision';",
      "const currentTaskWindow = (prompt.match(/currentTask[\\s\\S]{0,1400}/i)?.[0] ?? prompt);",
      "const agentKey = process.env.AGENT_CLUSTER_AGENT_KEY ?? '';",
      "const isFrontend = agentKey === 'frontend' || /Frontend implementation/i.test(currentTaskWindow) || /key\\s*:?\\s*frontend/i.test(currentTaskWindow);",
      "const agentName = isFrontend ? 'frontend' : 'backend';",
      'if (isClaim) {',
      '  console.log(JSON.stringify({',
      "    kind: 'task_claim_decision',",
      '    accepted: true,',
      "    reason: `${agentName} Codex runtime accepts this coding task.`,",
      '    confidence: 0.94,',
      "    agentMessages: [{ kind: 'agent_message', messageKind: 'decision', content: `${agentName} accepts and will edit source files.`, targetAgentKeys: ['coordinator'] }]",
      '  }));',
      '} else {',
      "  mkdirSync('src', { recursive: true });",
      "  const file = isFrontend ? 'src/frontend.txt' : 'src/backend.txt';",
      "  const generated = isFrontend ? 'src/frontend-generated.txt' : 'src/backend-generated.txt';",
      "  writeFileSync(file, `${agentName} after real codex runtime\\n`);",
      "  writeFileSync(generated, `generated by ${agentName} codex runtime\\n`);",
      '  console.log(JSON.stringify({',
      "    kind: 'task_execution_result',",
      "    status: 'completed',",
      "    summary: `${agentName} Codex runtime edited real files on disk.`,",
      "    completedItems: [`${agentName} changed source files`],",
      '    changedArtifacts: [],',
      "    agentMessages: [{ kind: 'agent_message', messageKind: 'progress', content: `${agentName} requests peer review for the captured source diff.`, targetAgentKeys: ['review', 'test'] }],",
      "    nextSuggestedActions: ['Inspect captured fileChanges and test report'],",
      '    risks: []',
      '  }));',
      '}'
    ].join('\n')
  );
  if (process.platform === 'win32') {
    await writeFile(stubCommand, `@echo off\r\nnode "%~dp0codex-multi-stub.mjs" %*\r\n`);
  } else {
    await writeFile(stubCommand, `#!/bin/sh\nnode "$(dirname "$0")/codex-multi-stub.mjs" "$@"\n`);
  }

  server = await startSmokeServer('multi-codex-runtime-agents-smoke', {
    DISCUSSION_MAX_ROUNDS: '0',
    MOCK_PARALLEL_TASKS: 'true',
    MOCK_DECLINE_FRONTEND_TASK: 'false',
    REQUIRE_USER_CONFIRMATION: 'false',
    CODEX_RUNTIME_ENABLED: 'true',
    CODEX_RUNTIME_COMMAND: 'node',
    CODEX_RUNTIME_ARGS_JSON: JSON.stringify([stubScript]),
    CODEX_RUNTIME_PROMPT_MODE: 'file',
    CODEX_RUNTIME_SHELL: 'false',
    CODEX_RUNTIME_TEST_COMMAND: 'npm test'
  });

  const frontendAgent = await api(server.apiBase, '/agents/frontend', {
    method: 'PATCH',
    body: JSON.stringify({ runtimeType: 'codex' })
  });
  const backendAgent = await api(server.apiBase, '/agents/backend', {
    method: 'PATCH',
    body: JSON.stringify({ runtimeType: 'codex' })
  });
  await api(server.apiBase, '/agents/test', {
    method: 'PATCH',
    body: JSON.stringify({ runtimeType: 'mock' })
  });
  await api(server.apiBase, '/agents/review', {
    method: 'PATCH',
    body: JSON.stringify({ runtimeType: 'mock' })
  });

  if (frontendAgent.data.runtimeType !== 'codex' || backendAgent.data.runtimeType !== 'codex') {
    throw new Error(
      `Expected frontend/backend agents to use codex runtime, got ${frontendAgent.data.runtimeType}/${backendAgent.data.runtimeType}`
    );
  }

  const { sessionId, briefId } = await createSessionAndWaitForBrief(
    server.apiBase,
    'Use multiple Codex coding agents to edit frontend and backend source files, communicate with reviewers, run tests, and expose diffs.',
    {
      agentIds: ['coordinator', 'requirements', 'architect', 'frontend', 'backend', 'test', 'review', 'notification'],
      workingDirectory: {
        kind: 'server_local',
        id: '00000000-0000-0000-0000-00000000c0de',
        name: 'multi-codex-workspace',
        path: workspaceRoot,
        selectedAt: new Date().toISOString()
      }
    }
  );
  await api(server.apiBase, `/sessions/${sessionId}/briefs/${briefId}/confirm`, { method: 'POST' });
  await waitForStatus(server.apiBase, sessionId, 'COMPLETED', 90_000);

  const events = await listEvents(server.apiBase, sessionId);
  const eventDebug = () =>
    JSON.stringify(
      events
        .filter((event) =>
          ['task_created', 'task_claimed', 'task_started', 'runtime_completed', 'runtime_failed', 'artifact_created'].includes(
            event.type
          )
        )
        .map((event) => ({
          type: event.type,
          taskId: event.taskId,
          fromAgentId: event.fromAgentId,
          runtimeType: event.metadata.payload?.runtimeType,
          title: event.metadata.payload?.title,
          summary: event.metadata.payload?.contentSummary,
          fileChanges: (event.metadata.payload?.fileChanges ?? []).map((item) => ({
            path: item.path,
            operation: item.operation,
            source: item.source
          })),
          message: event.metadata.payload?.message
        })),
      null,
      2
    );
  const codexCompletions = events.filter(
    (event) => event.type === 'runtime_completed' && event.metadata.payload?.runtimeType === 'codex'
  );
  const codexTaskIds = new Set(codexCompletions.map((event) => event.taskId).filter(Boolean));
  if (codexTaskIds.size < 2) {
    throw new Error(`Expected at least two Codex coding tasks, got ${codexTaskIds.size}. Events: ${eventDebug()}`);
  }

  const acceptedClaims = events.filter(
    (event) =>
      event.type === 'agent_message' &&
      event.metadata.payload?.phase === 'task_acceptance_decision' &&
      event.metadata.payload?.acceptanceDecision?.status === 'accepted' &&
      event.metadata.payload?.claimDecision?.accepted === true
  );
  if (acceptedClaims.length < 2) {
    throw new Error(`Expected two task acceptance decisions, got ${acceptedClaims.length}`);
  }

  const runtimeCommunications = events.filter(
    (event) => event.type === 'agent_message' && event.metadata.payload?.phase === 'agent_runtime_communication'
  );
  if (runtimeCommunications.length < 2) {
    throw new Error(`Expected both coding agents to communicate during runtime, got ${runtimeCommunications.length}`);
  }

  const fileChanges = events
    .filter((event) => event.type === 'artifact_created' && codexTaskIds.has(event.taskId))
    .flatMap((event) => event.metadata.payload?.fileChanges ?? []);
  for (const path of ['src/frontend.txt', 'src/backend.txt', 'src/frontend-generated.txt', 'src/backend-generated.txt']) {
    const change = fileChanges.find((item) => item.path === path && item.source === 'actual_filesystem_snapshot');
    if (!change) {
      throw new Error(
        `Expected captured actual filesystem change for ${path}. Captured changes: ${JSON.stringify(
          fileChanges.map((item) => ({ path: item.path, operation: item.operation, source: item.source })),
          null,
          2
        )}\nEvents: ${eventDebug()}`
      );
    }
  }

  const testReports = events.filter(
    (event) => event.type === 'artifact_created' && codexTaskIds.has(event.taskId)
  ).flatMap(
    (event) =>
      (event.metadata.payload?.runtimeArtifacts ?? []).filter(
        (artifact) => artifact.type === 'test_report' && artifact.metadata?.status === 'completed'
      )
  );
  if (testReports.length < 2) {
    throw new Error(`Expected a completed test report for each Codex coding task, got ${testReports.length}`);
  }

  const frontendFile = await readFile(join(workspaceRoot, 'src', 'frontend.txt'), 'utf8');
  const backendFile = await readFile(join(workspaceRoot, 'src', 'backend.txt'), 'utf8');
  if (!frontendFile.includes('frontend after real codex runtime') || !backendFile.includes('backend after real codex runtime')) {
    throw new Error('Expected both real source files to be modified on disk.');
  }

  console.log('multi codex runtime agents smoke ok');
} finally {
  if (server) {
    await stopSmokeServer(server);
  }
  if (workspaceRoot) {
    await rm(workspaceRoot, { recursive: true, force: true });
  }
}
